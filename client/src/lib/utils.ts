import { type ClassValue, clsx } from "clsx";
import { twMerge } from "tailwind-merge";
import { PlayerWithRegistrationId } from "./types";

export function cn(...inputs: ClassValue[]) {
  return twMerge(clsx(inputs));
}

export function formatDate(date: Date): string {
  return new Intl.DateTimeFormat('en-US', {
    year: 'numeric',
    month: 'long',
    day: 'numeric',
    hour: '2-digit',
    minute: '2-digit'
  }).format(new Date(date));
}

// Generate a unique registration ID
export function generateRegistrationId(player: any): PlayerWithRegistrationId {
  // Create a registration ID based on player info
  const prefix = "FF";
  const namePart = player.inGameName.substring(0, 3).toUpperCase();
  const randomNum = Math.floor(Math.random() * 10000).toString().padStart(4, '0');
  const registrationId = `${prefix}-${namePart}-${randomNum}`;
  
  return {
    ...player,
    registrationId
  };
}

